CREATE DATABASE Airline_Union;
USE	Airline_Union;

CREATE TABLE Customers -- 01 创建购票用户表
(
    CID VARCHAR(20) PRIMARY KEY, -- 用户ID
    Cname VARCHAR(50) NOT NULL, -- 用户名
    Realname VARCHAR(50) NOT NULL, -- 姓名
    TelephoneNum VARCHAR(20) NOT NULL, -- 电话号码
    Csid CHAR(18) NOT NULL, -- 用户身份证号
    Csex CHAR(1) NOT NULL, -- 性别
    Password VARCHAR(50) NOT NULL, -- 密码
    Points INT DEFAULT 0 -- 积分点数
);

CREATE TABLE FlightsManagers -- 02 创建航司管理员表
(
    FMID VARCHAR(20) PRIMARY KEY, -- 航司管理员ID
    FCname VARCHAR(50) NOT NULL, -- 航司名
    Fpassword VARCHAR(50) NOT NULL, -- 密码
    Fname VARCHAR(50) NOT NULL, -- 姓名
    Fsex CHAR(1) NOT NULL -- 性别
);

CREATE TABLE Airports -- 03 创建机场表
(
    Aname VARCHAR(50) PRIMARY KEY, -- 机场名
    City VARCHAR(50) NOT NULL -- 城市
);

CREATE TABLE AirportsManagers -- 04 创建机场管理员表
(
    AMID VARCHAR(20) PRIMARY KEY, -- 机场管理员ID
    AMpassword VARCHAR(50) NOT NULL, -- 密码
    AMname VARCHAR(50) NOT NULL, -- 姓名
    AMsex CHAR(1) NOT NULL, -- 性别
    Aname VARCHAR(50) NOT NULL, -- 机场名
    FOREIGN KEY (Aname) REFERENCES Airports(Aname)
);

CREATE TABLE Passengers -- 创建乘客表
(
    Order_ID VARCHAR(20),
    PSid CHAR(18) NOT NULL, -- 乘客身份证号
    Pname VARCHAR(50) NOT NULL, -- 姓名
    Psex CHAR(1) NOT NULL, -- 性别
    PRIMARY KEY (Order_ID, PSid),
    FOREIGN KEY (Order_ID) REFERENCES Orders(OrdersID),
    UNIQUE KEY (Order_ID, PSid)  -- 为 PSid 创建唯一索引
);

CREATE TABLE Orders -- 06 创建订单表
(
    OrdersID VARCHAR(20) PRIMARY KEY, -- 订单编号
    FlightsID VARCHAR(20), -- 航班编号
    Pay DECIMAL(10, 2) NOT NULL, -- 支付金额
    Status CHAR(1) NOT NULL, -- 是否支付
    PaymentTime DATETIME , -- 支付时间
    CID VARCHAR(20), -- 用户ID
    FOREIGN KEY (CID) REFERENCES Customers(CID)
);

CREATE TABLE Flights -- 07 创建航班表
(
    FlightsID VARCHAR(20) PRIMARY KEY, -- 航班编号
    DeparturePlace VARCHAR(50) NOT NULL, -- 起飞地点
    ArrivalPlace VARCHAR(50) NOT NULL, -- 到达地点
    DepartureAirport VARCHAR(50), -- 起飞机场
    ArrivalAirport VARCHAR(50), -- 到达机场
    Departuredate DATE, -- 起飞日期 可以为空 机场管理员修改
    Takeofftime TIME, -- 起飞时间 可以为空 机场管理员修改
    TotalTime TIME, -- 航班总时长
    PlantType VARCHAR(50), -- 飞机型号
    Fprice INT -- 航班票价
);

CREATE TABLE Tickets -- 08 创建机票表
(
    FlightsID VARCHAR(20), -- 航班编号
    SeatNum VARCHAR(10), -- 座位号
    OrdersID VARCHAR(20), -- 订单编号
    PSid CHAR(18), -- 乘客身份证号
    TicketPrice DECIMAL(10, 2), -- 机票价格
    PRIMARY KEY (FlightsID, SeatNum),
    FOREIGN KEY (OrdersID) REFERENCES Orders(OrdersID),
    FOREIGN KEY (OrdersID, PSid) REFERENCES Passengers(Order_ID, PSid)
);


